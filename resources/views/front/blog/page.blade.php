          <p>В каталоге Kastas приведена таблица допусков на диаметр отверстия, на диаметр штока и номинальный размер. Численные значения допусков приведены в микрометрах (мкм). Соотношение между мм и мкм следующее: <b>1 мм = 100 мкм</b>. При этом квалитеты точности для отверстий обозначаются заглавными (прописными) латинскими буквами, для штоков (валов) малыми (строчными) латинскими буквами.</p>

          <p>Например, при номинальном размере отверстия в 50 мм верхний предел (допуск) будет составлять 50,46 мм по квалитету Н8 и 53,00 мм по квалитету Н12. При этом по нижнему пределу (допуску) значение будет 50,00 мм для обоих квалитетов точности.</p>
          <p>При номинальном диаметре штока (вала) в 50 мм нижний предел (допуск) будет составлять 49.54 мм по квалитету f7 и 47,00 мм по квалитету h11. При этом по верхнему пределу (допуску) значение будет 50,00 мм для обоих квалитетов точности.</p>
          <p>Следует отметить, что основные поставщики готовых штоков и труб гидроцилиндров придерживаются квалитетов точности H8 и H9 по гильзам цилиндров и f7 и f8 по готовым штокам.
