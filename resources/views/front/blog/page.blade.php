          <p>Например, к нам недавно обратился заказчик с гидроцилиндром рукояти колесного экскаватора Хюндай с диметром штока 95 мм, диаметром гильзы цилиндра 135 мм. Мы укомплектовали его грязесъемником со стальным кольцом  К12, запрессовав его в открытую канавку. Установили воротниковую манжету К22, буферное уплотнение К29.</p>
          <p>В качестве направляющего элемента установлена стальная втулка с тефлоновым покрытием К780. Снаружи установлены обычные кольца круглого сечения  вместе с пластиковым подпором. На поршне мы установили манжету К504, два направляющих кольца К73 и два направляющих кольца из PTFE, используемые для захвата твердых частиц из гидросистемы. Таким образом анализируя комплектацию уплотнениями можно сделать вывод, что крышка цилиндра укомплектована манжетами для среднего режима, а поршень – для тяжелого режима работы.</p>

